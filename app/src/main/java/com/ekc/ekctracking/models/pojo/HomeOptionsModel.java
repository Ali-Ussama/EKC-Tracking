package com.ekc.ekctracking.models.pojo;

public class HomeOptionsModel {

    private int id;

    private int reportIconResId;

    private String reportName;

    public HomeOptionsModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getReportIconResId() {
        return reportIconResId;
    }

    public void setReportIconResId(int reportIconResId) {
        this.reportIconResId = reportIconResId;
    }

    public String getReportName() {
        return reportName;
    }

    public void setReportName(String reportName) {
        this.reportName = reportName;
    }
}
